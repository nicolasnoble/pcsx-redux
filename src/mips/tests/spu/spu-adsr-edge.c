#define SPU_ADSR_EDGE_MACROS_DEFINED
#define ATTACK_RATE(rate, exp) ATTACK((rate) & 3, ((rate) >> 2) & 31, exp)
#define SUSTAIN_RATE(rate, level, direction, exp) SUSTAIN((rate) & 3, ((rate) >> 2) & 31, level, direction, exp)
#define RELEASE_RATE(rate, exp) RELEASE(((rate) >> 2) & 31, exp)
#define SPU_ADSR_EDGE_RUN(name, adsr, mode, samples, keyoff, first, last, minmax, hash) \
    spu_adsr_edge_run_trace(name, adsr, mode, samples, keyoff, first, last, minmax, hash)
